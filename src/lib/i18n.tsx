const dicts: Record<Lang, Dict> = { en, hi, mr };

export type TT = { en: string; hi?: string; mr?: string };

const Ctx = createContext<{
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (k: string) => string;
  tt: (o: TT | string | undefined | null) => string;
}>({
  lang: "en",
  setLang: () => {},
  t: (k) => k,
  tt: (o) => (o ? (typeof o === "string" ? o : o.en) : ""),
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");
  useEffect(() => {
    const saved = (typeof window !== "undefined" && localStorage.getItem("nourish-lang")) as Lang | null;
    if (saved && dicts[saved]) setLangState(saved);
  }, []);
  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") localStorage.setItem("nourish-lang", l);
  };
  const t = (k: string) => dicts[lang][k] ?? dicts.en[k] ?? k;
  const tt = (o: TT | string | undefined | null) => {
    if (!o) return "";
    if (typeof o === "string") return o;
    return lang === "en" ? o.en : (o[lang] ?? o.en);
  };
  return <Ctx.Provider value={{ lang, setLang, t, tt }}>{children}</Ctx.Provider>;
}

export const useI18n = () => useContext(Ctx);

